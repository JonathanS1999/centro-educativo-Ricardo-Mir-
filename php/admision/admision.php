<h4 class="h4AD">Proceso de Matricula</h4>
<p class="pAD">
Los periodos de matrículas son anuales y nos ajustamos al cronogromas de 
matricula por año, enviado por MEDUCA las fechas oproximadamente las dos últimas 
semanos del mes de septiembre para preescolar, inicio de octubre para la sección de 
Primaria y Premedia, en el mes de octubre la matrícula es solamente para los 7°. </p>

<h5 class="h5AD"> Requisitos de matrícula: </h5>

<h6 class="h6AD">Educación preescolar </h6>

<p class="pAD">2 Fotos tamaño carnet <br/>

 Copias de cédula del estudiante por ambas caras<br/>
 
 2 Copias de cédula del acudiente <br/>

 Copia de la tarjeta de vacuna <br/>

 Llenar el Formulario de Matricula <br/>

<h6 class="h6AD">Educación Primaria</h6>

<p class="pAD"> Boletín del año correspondiente <br/>
 Registro de calificaciones o modelo F <br/>
  2 Fotos amaño carnet <br/>
  Copias de cédula del estudiante por ambas caras<br/>
   2 Copias de cédula del acudiente <br/>
   Copia de la tarjeta de vacuna <br/>
    Llenar el Formulario de Matricula 

<h6 class="h6AD"> Educación Premedia</h6>
<p class="pAD">Boletín del año correspondiente <br/>
Crédito: de años anteriores <br/>
 2 Copia de cédula del estudiante por ambas caras<br/>
  2 Copia: de cédula del acudiente <br/>
   Certificado de salud <br/>
   2 fotos amaño carnet </p>


