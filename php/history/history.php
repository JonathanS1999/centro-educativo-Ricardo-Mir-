     <p> La escuela Ricardo Miró está ubicada en la comunidad de Vista Hermosa, 
        corregimiento de Pueblo Nuevo en el distrito de Panamá.</p>
     <p> Surgió en 1938 con el nombre de Vista Hermosa, bajo la dirrección de la profesora 
        Eladia Solanilla de Royer, se inició en casa de alquiler, habitadas como aulas de clases.
     </p>
     <p>Durante la administración del presidente Alcides Arosemena y siendo ministro de educación 
        el profesor Rubén Dario Carles, se aprobó el decreto 573 de 26 de noviembre de 1951 que cambió el nombre de 
        Escuela Vista Hermosa por Escuela Ricardo Miró.
     </p>
     <p>Se asignó el nombre del insigne poeta panameño para horrar su memoria y su legado poetico que supo recoger el 
        profundo sentimiento patriótico que anida en el corazón de todo panameño y que se recoge en los versos de las mas
        significativa de las composiciones patrióticas panameñas, el poema Patria.
     </p>
     <p>Al crecer la población escolar en 1953 se hicieron gestiones para un edificio
        propio y se logró conseguir dos pabellones de madera que la Zona del Canal en aquel tiempo, 
        vendió al gobierno panameño a un valor simbólico.
     </p>
     <p> En 1973, por la condición peligrosa en que se encontraban estos pabellones fueron
        demolidos para ser reemplazados por el actual edificio, que actualmente le conocemos como 
        el "edificio antiguo" para diferenciarlo del otro, el cual fué contruido recientemente y se utilizó por primera
        vez en el año escolar 2019.
     </p>
     <p> En la inauguración de 1973, el corte de cinta le correspondió al H.R de esos años el señor 
        Pedro Nolasco Segura y al Doctor Aristides Royo como ministro de educación, develar la placa conmemorativa de dicho acto.
     </p>

